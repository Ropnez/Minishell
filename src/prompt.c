#include "minishell.h"
#include <readline/readline.h>
#include <readline/history.h>
#include <stdlib.h>

char	*prompt_input(t_env *minienv)
{
	char	*input;

	input = readline("minishell$ ");
	if (!input)
		builtin_exit(NULL, &minienv);
	if (input && *input)
		add_history(input);
	return (input);
}
